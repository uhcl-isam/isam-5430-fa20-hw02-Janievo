using System;

namespace Selection_Test
{
    class Program
    {
        static void Main(string[] args)
        {
            public static bool CigarParty(int cigars, bool isWeekend)
            {
                if (isWeekend && cigars >= 40)
                {
                    return true;
                }
                else if (cigars >= 40 && cigars <= 60)
                {
                    return true;
                }
                return false;
            }
        }
    }
}
